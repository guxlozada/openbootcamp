package org.example;

public class EjerciciosTema1 {

    public static void main(String[] args) {
        // Numericos
        byte variable1=5;
        System.out.printf("byte:%d%n", variable1);
        short variable2=10;
        System.out.printf("short:%d%n", variable2);
        int variable3=15;
        System.out.printf("int:%d%n", variable3);
        long variable4=20;
        System.out.printf("long:%d%n", variable4);

        // Decimales
        float variable5= 1.1f;
        System.out.printf("float:%f%n", variable5);
        double variable6= 2.2d;
        System.out.printf("double:%f%n", variable6);

        // Booleano
        boolean variable7 =true;
        System.out.printf("boolean:%b%n", variable7);
        boolean variable8 =false;
        System.out.printf("boolean2:%b%n", variable8);

        //Texto
        char variable9 ='a';
        System.out.printf("char:%s%n", variable9);
        String variable10 ="texto";
        System.out.printf("String:%s", variable10);
    }
}
